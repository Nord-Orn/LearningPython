# Метод разбиения строки по разделителю в список.
S = 'Cat, Dog,Hamster   Rabbit, Pig'
print(S.split())  # ['Cat,', 'Dog,Hamster', 'Rabbit,', 'Pig'] - разбивает строку
# по пробельным символам — пробелу и знаку табуляции
print(S.split(','))  # ['Cat', ' Dog', 'Hamster   Rabbit', ' Pig'] - Разбивает строку
# по заданному разделителю ','
print(S.split(',', 2))  # - ['Cat', ' Dog', 'Hamster   Rabbit, Pig']
# Задаёт максимальное количество разбиений, равное 3
